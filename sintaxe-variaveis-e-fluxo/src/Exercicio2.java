
public class Exercicio2 {
	public static void main(String[] args) {
		
		System.out.println("Fatorial de 1 a 10");
		
		for(int n = 1 ; n <= 10 ; n++) {
			
			System.out.println();
			
			for(int fatorial = 1 ; fatorial <= 10 ; fatorial++) {
				
			System.out.println(n * fatorial);
			System.out.println(" ");
				
			}
			System.out.println();
		
		}
		
	}

}
